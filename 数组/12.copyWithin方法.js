// copyWithin方法,会修改原有数组,但不会影响原数组的长度.
let arr = [1,2,3,4,5,6,7,8,9,0];

// 实际理解:先拷贝原有数组,再把从指定下标(这里是3)开始的数组元素复制并从指定下标(这里是0)开始逐一覆盖.
console.log(arr.copyWithin(0,3));
    /* [
        4, 5, 6, 7, 8,
        9, 0, 8, 9, 0
    ] */


// 从下标3到下标5之前(不包括5)的元素复制,然后从下标1开始依次覆盖.
let arr2 = [1,2,3,4,5,6,7,8,9,0];
console.log(arr2.copyWithin(1,3,5));
    /* [
        1, 4, 5, 4, 5,
        6, 7, 8, 9, 0
        ] */

    console.log(arr2);
    /* [
        1, 4, 5, 4, 5,
        6, 7, 8, 9, 0
        ]*/
    